
import java.util.Scanner;

public class Solution {

    public static void main(String[] args) {
        int t, a, b;
        Scanner sc = new Scanner(System.in);
        t = sc.nextInt();
        while (t-- > 0) {
            a = sc.nextInt();
            b = sc.nextInt();
            System.out.println(a + b);
        }
    }
}

